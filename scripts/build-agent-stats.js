/**
 * 汇总 agent-picks-day*.json 结算 → js/agent-stats.js（过往赛果页展示）
 * Run: node scripts/build-agent-stats.js
 * 通常在 grade-agent-picks.js 赛后自动调用
 */
const fs = require('fs');
const path = require('path');

const ROOT = path.join(__dirname, '..');
const OUT = path.join(ROOT, 'js', 'agent-stats.js');
const TS = new Date().toISOString().replace(/\.\d{3}Z$/, '+08:00');

function mergeBucket(a, b) {
  return {
    hit: (a.hit || 0) + (b.hit || 0),
    miss: (a.miss || 0) + (b.miss || 0),
    push: (a.push || 0) + (b.push || 0),
    pending: (a.pending || 0) + (b.pending || 0),
  };
}

function withPct(b) {
  const n = (b.hit || 0) + (b.miss || 0);
  return {
    ...b,
    n,
    pct: n ? Math.round(((b.hit || 0) / n) * 1000) / 10 : null,
  };
}

function loadDayFiles() {
  return fs.readdirSync(__dirname)
    .filter(f => /^agent-picks-day\d+\.json$/.test(f))
    .sort()
    .map(f => {
      const data = JSON.parse(fs.readFileSync(path.join(__dirname, f), 'utf8'));
      return { file: f, ...data };
    });
}

function build() {
  const days = loadDayFiles();
  const gradedDays = days.filter(d => d.results?.spread);
  const byId = {};
  const daysSummary = [];

  let spread = { hit: 0, miss: 0, push: 0, pending: 0 };
  let totals = { hit: 0, miss: 0, push: 0, pending: 0 };
  let primary = { hit: 0, miss: 0, push: 0, pending: 0 };
  let secondary = { hit: 0, miss: 0, push: 0, pending: 0 };
  let highSpread = { hit: 0, miss: 0 };
  let highTotals = { hit: 0, miss: 0 };
  let highPrimary = { hit: 0, miss: 0 };
  const highIds = [];

  for (const day of gradedDays) {
    const r = day.results;
    spread = mergeBucket(spread, r.spread);
    totals = mergeBucket(totals, r.totals);
    primary = mergeBucket(primary, r.primary);
    secondary = mergeBucket(secondary, r.secondary);

    if (r.high_confidence) {
      highSpread = mergeBucket(highSpread, r.high_confidence.spread);
      highTotals = mergeBucket(highTotals, r.high_confidence.totals);
      highPrimary = mergeBucket(highPrimary, r.high_confidence.primary);
      highIds.push(...(r.high_confidence.ids || []));
    }

    for (const g of r.picks || []) {
      byId[g.id] = {
        day: day.day,
        match: g.match,
        primary: g.primary,
        high_confidence: !!g.high_confidence,
        confidence_cn: g.confidence_cn || null,
        spread: g.spread,
        totals: g.totals,
        primary_hit: g.primary_hit,
        spread_hit: g.spread?.hit,
        totals_hit: g.totals?.hit,
        grade_note_spread: g.spread?.grade_note,
        grade_note_totals: g.totals?.grade_note,
        actual_score: g.actual_score,
        outcome: g.outcome,
      };
    }

    daysSummary.push({
      day: day.day,
      date: day.date,
      graded_at: r.graded_at,
      spread: withPct(r.spread),
      totals: withPct(r.totals),
      primary: withPct(r.primary),
      high_confidence: r.high_confidence ? {
        n: r.high_confidence.n,
        ids: r.high_confidence.ids,
        spread: withPct(r.high_confidence.spread),
        totals: withPct(r.high_confidence.totals),
        primary: withPct(r.high_confidence.primary),
      } : null,
    });
  }

  const pendingDays = days.filter(d => !d.results?.spread).map(d => ({
    day: d.day,
    date: d.date,
    pick_ids: (d.picks || []).map(p => p.id),
  }));

  const payload = {
    lastUpdated: TS,
    grade_rule_cn: gradedDays[0]?.results?.grade_rule_cn || '不败即中：全赢/赢半/走水算中，输半/全输算不中',
    days_graded: gradedDays.length,
    days_pending: pendingDays.length,
    aggregate: {
      spread: withPct(spread),
      totals: withPct(totals),
      primary: { ...withPct(primary), label_cn: '★倾向项' },
      secondary: { ...withPct(secondary), label_cn: '非倾向项' },
      high_confidence: {
        n: highIds.length,
        ids: highIds,
        spread: withPct(highSpread),
        totals: withPct(highTotals),
        primary: withPct(highPrimary),
      },
    },
    days: daysSummary,
    pending: pendingDays,
    byId,
  };

  const js = `// Auto-generated by scripts/build-agent-stats.js — DO NOT EDIT
// Last updated: ${TS}
const AGENT_STATS = ${JSON.stringify(payload, null, 2)};
`;
  fs.writeFileSync(OUT, js);
  console.log('✅ agent-stats.js —', Object.keys(byId).length, '场已结算 · 大信心', highIds.length, '场');
  if (pendingDays.length) {
    console.log('   待结算:', pendingDays.map(d => `Day${d.day} (${d.pick_ids.join(', ')})`).join(' · '));
  }
  return payload;
}

if (require.main === module) build();
module.exports = { build };
